    @Override
    public String visitInvokeExpr(InvokeExprContext ctx) {
        InvokeExpressionContext invoke = ctx.invokeExpression();
        return generateInvokeCall(invoke.serviceRef(), invoke.argumentList());
    }

    @Override
    public String visitDotAccess(DotAccessContext ctx) {
        String left = visit(ctx.expression());
        String field = ctx.IDENTIFIER().getText();
        return "PipelineContext.getField(" + left + ", \"" + escape(field) + "\")";
    }

    @Override
    public String visitNullSafeAccess(NullSafeAccessContext ctx) {
        String left = visit(ctx.expression());
        String field = ctx.IDENTIFIER().getText();
        return "PipelineContext.getFieldSafe(" + left + ", \"" + escape(field) + "\")";
    }

    @Override
    public String visitArrayProjection(ArrayProjectionContext ctx) {
        String left = visit(ctx.expression());
        String field = ctx.IDENTIFIER().getText();
        return "PipelineContext.project(" + left + ", \"" + escape(field) + "\")";
    }

    @Override
    public String visitIndexAccess(IndexAccessContext ctx) {
        String left = visit(ctx.expression(0));
        String index = visit(ctx.expression(1));
        return "arrayGet(" + left + ", " + index + ")";
    }

    @Override
    public String visitFilterAccess(FilterAccessContext ctx) {
        String left = visit(ctx.expression(0));
        String field = ctx.IDENTIFIER().getText();
        String value = visit(ctx.expression(1));
        return "PipelineContext.filter(" + left + ", \"" + escape(field) + "\", " + value + ")";
    }

    @Override
    public String visitNullCoalesceExpr(NullCoalesceExprContext ctx) {
        String left = visit(ctx.expression(0));
        String right = visit(ctx.expression(1));
        return "PipelineContext.coalesce(" + left + ", " + right + ")";
    }

    @Override
    public String visitTernaryExpr(TernaryExprContext ctx) {
        String cond = visit(ctx.expression(0));
        String trueVal = visit(ctx.expression(1));
        String falseVal = visit(ctx.expression(2));
        return "(toBoolean(" + cond + ") ? " + trueVal + " : " + falseVal + ")";
    }

    @Override
    public String visitComparisonExpr(ComparisonExprContext ctx) {
        String left = visit(ctx.expression(0));
        String right = visit(ctx.expression(1));
        String op = ctx.getChild(1).getText();
        switch (op) {
            case "==": return "eq(" + left + ", " + right + ")";
            case "!=": return "!eq(" + left + ", " + right + ")";
            case "<":  return "cmp(" + left + ", " + right + ") < 0";
            case ">":  return "cmp(" + left + ", " + right + ") > 0";
            case "<=": return "cmp(" + left + ", " + right + ") <= 0";
            case ">=": return "cmp(" + left + ", " + right + ") >= 0";
            default:   return left + " " + op + " " + right;
        }
    }

    @Override
    public String visitAndExpr(AndExprContext ctx) {
        return "toBoolean(" + visit(ctx.expression(0)) + ") && toBoolean(" + visit(ctx.expression(1)) + ")";
    }

    @Override
    public String visitOrExpr(OrExprContext ctx) {
        return "toBoolean(" + visit(ctx.expression(0)) + ") || toBoolean(" + visit(ctx.expression(1)) + ")";
    }

    @Override
    public String visitNotExpr(NotExprContext ctx) {
        return "!toBoolean(" + visit(ctx.expression()) + ")";
    }

    @Override
    public String visitAddSubExpr(AddSubExprContext ctx) {
        String left = visit(ctx.expression(0));
        String right = visit(ctx.expression(1));
        String op = ctx.getChild(1).getText();
        if ("+".equals(op)) {
            return "add(" + left + ", " + right + ")";
        }
        return "PipelineContext.toNum(" + left + ") " + op + " PipelineContext.toNum(" + right + ")";
    }

    @Override
    public String visitMulDivExpr(MulDivExprContext ctx) {
        String left = visit(ctx.expression(0));
        String right = visit(ctx.expression(1));
        String op = ctx.getChild(1).getText();
        return "PipelineContext.toNum(" + left + ") " + op + " PipelineContext.toNum(" + right + ")";
    }

    @Override
    public String visitStringLiteral(StringLiteralContext ctx) {
        return ctx.getText();
    }

    @Override
    public String visitTemplateString(TemplateStringContext ctx) {
        // f"Hello {name}, total: {order.amount + tax}" -> string concatenation with compiled expressions
        String raw = ctx.getText();
        // Remove f" prefix and " suffix
        raw = raw.substring(2, raw.length() - 1);
        StringBuilder sb = new StringBuilder("\"\"");
        int i = 0;
        while (i < raw.length()) {
            if (raw.charAt(i) == '{') {
                int end = findMatchingBrace(raw, i);
                String exprText = raw.substring(i + 1, end);
                // Re-parse the expression through the WmScript compiler
                String compiledExpr = WmScriptCompiler.compileExpression(exprText);
                sb.append(" + PipelineContext.toStr(").append(compiledExpr).append(")");
                i = end + 1;
            } else {
                int next = raw.indexOf('{', i);
                if (next < 0) next = raw.length();
                sb.append(" + \"").append(escape(raw.substring(i, next))).append("\"");
                i = next;
            }
        }
        return sb.toString();
    }

    /** Find the matching '}' for a '{', handling nested braces */
    private int findMatchingBrace(String s, int openPos) {
        int depth = 0;
        for (int i = openPos; i < s.length(); i++) {
            if (s.charAt(i) == '{') depth++;
            else if (s.charAt(i) == '}') {
                depth--;
                if (depth == 0) return i;
            }
        }
        return s.length(); // fallback: end of string
    }

    @Override
    public String visitNumberLiteral(NumberLiteralContext ctx) {
        String text = ctx.getText();
        if (text.contains(".")) {
            return text;
        }
        return text;
    }

    @Override
    public String visitTrueLiteral(TrueLiteralContext ctx) {
        return "Boolean.TRUE";
    }

    @Override
    public String visitFalseLiteral(FalseLiteralContext ctx) {
        return "Boolean.FALSE";
    }

    @Override
    public String visitNullLiteral(NullLiteralContext ctx) {
        return "null";
    }

    @Override
    public String visitPipelineLiteral(PipelineLiteralContext ctx) {
        return "pipeline";
    }

    @Override
    public String visitDocumentLiteral(DocumentLiteralContext ctx) {
        StringBuilder sb = new StringBuilder("PipelineContext.createDocument(");
        boolean first = true;
        for (DocumentEntryContext entry : ctx.documentEntry()) {
            if (!first) sb.append(", ");
            // Key can be IDENTIFIER or STRING_LITERAL
            String key = entry.IDENTIFIER() != null
                ? "\"" + escape(entry.IDENTIFIER().getText()) + "\""
                : entry.STRING_LITERAL().getText();
            sb.append(key).append(", ").append(visit(entry.expression()));
            first = false;
        }
        sb.append(")");
        return sb.toString();
    }

    @Override
    public String visitArrayLiteral(ArrayLiteralContext ctx) {
        StringBuilder sb = new StringBuilder("new Object[] {");
        boolean first = true;
        for (ExpressionContext expr : ctx.expression()) {
            if (!first) sb.append(", ");
            sb.append(visit(expr));
            first = false;
        }
        sb.append("}");
        return sb.toString();
    }

    @Override
    public String visitVariableExpr(VariableExprContext ctx) {
        String name = ctx.target().getText();
        // Check if it has dots -> nested access
        List<String> parts = new ArrayList<>();
        for (var id : ctx.target().IDENTIFIER()) {
            parts.add(id.getText());
        }

        if (parts.size() == 1) {
            String varName = parts.get(0);
            if (localVars.contains(varName)) {
                return varName;
            }
            return "ctx.get(\"" + escape(varName) + "\")";
        }

        // Multi-part: order.customer.name -> PipelineContext.getPath(ctx.get("order"), "customer", "name")
        StringBuilder sb = new StringBuilder();
        String root = parts.get(0);
        if (localVars.contains(root)) {
            sb.append("PipelineContext.getPath(").append(root);
        } else {
            sb.append("PipelineContext.getPath(ctx.get(\"").append(escape(root)).append("\")");
        }
        for (int i = 1; i < parts.size(); i++) {
            sb.append(", \"").append(escape(parts.get(i))).append("\"");
        }
        sb.append(")");
        return sb.toString();
    }

    @Override
    public String visitParenExpr(ParenExprContext ctx) {
        return "(" + visit(ctx.expression()) + ")";
    }

    @Override
    public String visitBuiltinExpr(BuiltinExprContext ctx) {
        return visitBuiltinCall(ctx.builtinCall());
    }

    @Override
    public String visitBuiltinCall(BuiltinCallContext ctx) {
        String name = ctx.builtinName().getText();
        List<String> args = new ArrayList<>();
        for (ExpressionContext expr : ctx.expression()) {
            args.add(visit(expr));
        }

        switch (name) {
            case "num":    return "PipelineContext.toNum(" + args.get(0) + ")";
            case "str":    return "PipelineContext.toStr(" + args.get(0) + ")";
            case "int":    return "PipelineContext.toInt(" + args.get(0) + ")";
            case "date":   return "PipelineContext.toDate(" + args.get(0) + ", " + args.get(1) + ")";
            case "len":    return "PipelineContext.len(" + args.get(0) + ")";
            case "sum":    return "PipelineContext.sum(" + args.get(0) + ")";
            case "min":    return "PipelineContext.min(" + args.get(0) + ")";
            case "max":    return "PipelineContext.max(" + args.get(0) + ")";
            case "join":   return "PipelineContext.join(" + args.get(0) + ", " + args.get(1) + ")";
            case "split":  return "PipelineContext.split(" + args.get(0) + ", " + args.get(1) + ")";
            case "keys":     return "PipelineContext.keys(" + args.get(0) + ")";
            case "values":   return "PipelineContext.values(" + args.get(0) + ")";
            case "entries":  return "PipelineContext.entries(" + args.get(0) + ")";
            case "exists":   return "ctx.exists(" + args.get(0) + ")";
            case "typeof":   return "PipelineContext.typeOf(" + args.get(0) + ")";
            case "contains": return "PipelineContext.contains(" + args.get(0) + ", " + args.get(1) + ")";
            case "trim":     return "PipelineContext.trim(" + args.get(0) + ")";
            case "replace":  return "PipelineContext.replaceStr(" + args.get(0) + ", " + args.get(1) + ", " + args.get(2) + ")";
            case "sort":     return "PipelineContext.sortArray(" + args.get(0) + ")";
            case "map":      return "PipelineContext.mapArray(" + args.get(0) + ", " + args.get(1) + ")";
            case "filter":   return "PipelineContext.filterArray(" + args.get(0) + ", " + args.get(1) + ")";
            case "reduce":   return "PipelineContext.reduceArray(" + args.get(0) + ", " + args.get(1) + ", " + args.get(2) + ")";
            case "upper":    return "PipelineContext.toStr(" + args.get(0) + ").toUpperCase()";
            case "lower":    return "PipelineContext.toStr(" + args.get(0) + ").toLowerCase()";
            case "startsWith": return "PipelineContext.toStr(" + args.get(0) + ").startsWith(PipelineContext.toStr(" + args.get(1) + "))";
            case "endsWith": return "PipelineContext.toStr(" + args.get(0) + ").endsWith(PipelineContext.toStr(" + args.get(1) + "))";
            case "substring": return args.size() > 2
                ? "PipelineContext.toStr(" + args.get(0) + ").substring(PipelineContext.toInt(" + args.get(1) + "), PipelineContext.toInt(" + args.get(2) + "))"
                : "PipelineContext.toStr(" + args.get(0) + ").substring(PipelineContext.toInt(" + args.get(1) + "))";
            case "indexOf":  return "PipelineContext.toStr(" + args.get(0) + ").indexOf(PipelineContext.toStr(" + args.get(1) + "))";
            case "abs":      return "Math.abs(PipelineContext.toNum(" + args.get(0) + "))";
            case "round":    return "Math.round(PipelineContext.toNum(" + args.get(0) + "))";
            case "floor":    return "Math.floor(PipelineContext.toNum(" + args.get(0) + "))";
            case "ceil":     return "Math.ceil(PipelineContext.toNum(" + args.get(0) + "))";
            default:         return name + "(" + String.join(", ", args) + ")";
        }
    }

