    private String generateInvokeCall(ServiceRefContext serviceRef, ArgumentListContext argList) {
        String svcName = serviceRef.getText();
        StringBuilder sb = new StringBuilder();
        sb.append("PipelineContext.invoke(\"").append(escape(svcName)).append("\"");

        if (argList != null) {
            for (ArgumentContext arg : argList.argument()) {
                String paramName = arg.argumentName().getText();
                String value = visit(arg.expression());
                sb.append(", \"").append(escape(paramName)).append("\", ").append(value);
            }
        }

        sb.append(")");
        return sb.toString();
    }

    private String generateNestedSet(String target, String value) {
        String[] parts = target.split("\\.");
        if (parts.length == 2) {
            String root;
            if (localVars.contains(parts[0])) {
                root = parts[0];
            } else {
                root = "ctx.get(\"" + escape(parts[0]) + "\")";
            }
            return indent() + "PipelineContext.setField(" + root
                 + ", \"" + escape(parts[1]) + "\", " + value + ");\n";
        }

        // Deep nesting: navigate to parent, then set field
        StringBuilder sb = new StringBuilder();
        String parentVar = newTempVar("parent");
        sb.append(indent()).append("Object ").append(parentVar).append(" = ");

        String root;
        if (localVars.contains(parts[0])) {
            root = parts[0];
        } else {
            root = "ctx.get(\"" + escape(parts[0]) + "\")";
        }

        if (parts.length == 3) {
            sb.append("PipelineContext.getField(").append(root)
              .append(", \"").append(escape(parts[1])).append("\");\n");
        } else {
            sb.append("PipelineContext.getPath(").append(root);
            for (int i = 1; i < parts.length - 1; i++) {
                sb.append(", \"").append(escape(parts[i])).append("\"");
            }
            sb.append(");\n");
        }

        sb.append(indent()).append("PipelineContext.setField(").append(parentVar)
          .append(", \"").append(escape(parts[parts.length - 1]))
          .append("\", ").append(value).append(");\n");
        return sb.toString();
    }

    private static String escape(String s) {
        return s.replace("\\", "\\\\")
                .replace("\"", "\\\"")
                .replace("\n", "\\n")
                .replace("\r", "\\r")
                .replace("\t", "\\t");
    }
